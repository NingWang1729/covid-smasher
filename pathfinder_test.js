function dfs(row, col) {
    console.log(row, col);
    WORLD_MAP[row][col] = 1;

    if (WORLD_MAP[row][col] == 3) {
        return 
    }

    if (canMove(row - 1, col)) {
        dfs_move_que.push(2);
        dfs(row - 1, col);
    }
    if (canMove(row, col + 1)) {
        dfs_move_que.push(1);
        dfs(row, col + 1);
    }
    if (canMove(row + 1, col)) {
        dfs_move_que.push(3);
        dfs(row + 1, col);
    }
    if (canMove(row, col - 1)) {
        dfs_move_que.push(0);
        dfs(row, col - 1);
    }


}

