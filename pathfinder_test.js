function moveableSpaces(grid) {
    let row_col_arr = [];
    for (let i = 0; i < grid.length; i++) {
        for (let j = 0; j < grid[i].length; j++) {
            if (grid[i][j] != 1) {
                row_col_arr.push({row: i, col: j});
            }
        }
    }
    return row_col_arr
}

